import { createSlice } from "@reduxjs/toolkit";

const _counterSlice = createSlice({
  name: "counter",
  initialState: {
    value: 0,
  },
  reducers: {
    increment: (state) => {
      state.value += 1;
    },
    decrement: (state) => {
      state.value -= 1;
    },
    incrementByAmount: (state, action) => {
      state.value += action.payload;
    },
  },
});

export const { decrement, increment, incrementByAmount } =
  _counterSlice.actions;

export default _counterSlice.reducer;
